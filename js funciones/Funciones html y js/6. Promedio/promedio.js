function promedio(pnota1,pnota2,pnota3){
    let nota1=pnota1;
    let nota2=pnota2;
    let nota3=pnota3;
    let promedioN;
    let suma= nota1+nota2+nota3;
    promedioN= suma/3
    return promedioN;
}

